#include "3-calc.h"
/**
 * op_add - a function for finding the sum of a and b.
 * @a: an input integer.
 * @b: an input integer.
 * Return: returns the sum of a and b.
*/

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - a function for finding the difference of a and b.
 * @a: an input integer.
 * @b: an input integer.
 * Return: returns the difference of a and b.
*/

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 *  op_mul - a function for finding the the product of a and b
 * @a: an input integer.
 * @b: an input integer.
 * Return: returns the product of a and b.
*/

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 *  op_div - a function for finding the the division of a and b
 * @a: an input integer.
 * @b: an input integer.
 * Return: returns the result of the division of a by b.
*/

int op_div(int a, int b)
{
	return (a / b);
}


/**
 *  op_mod - a function for finding the remainder of the division of a by b.
 * @a: an input integer.
 * @b: an input integer.
 * Return: returns the remainder of the division of a by b.
 */

int op_mod(int a, int b)
{
	return (a % b);
}

