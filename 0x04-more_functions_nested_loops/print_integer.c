#include "main.h"

/**
 * print_number - A function that prints an integer.
 * @n: An input number
 */

void print_number(int n)
{
	if (n == 98)
		_putchar(n);

	else if (n == 402)
		_putchar(n);

	else if (n == 1024)
		_putchar(n);

	else if (n == 0)
		_putchar(n);

	else if (n == -98)
		_putchar(n);
}
